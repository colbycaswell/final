          <div class="col-md-6" data-aos="fade-up" data-aos-delay="500">
            <p>One way to search for exoplanets is to look for "wobbly" stars. A star that has planets doesn’t orbit perfectly around its center. From far away, this off-center orbit makes the star look like it’s wobbling.</p>
            <div class="row mb-5">
          <div class="col-12" data-aos="fade-up" data-aos-delay="300">
            <img src="images/wobble.gif" alt="Image" class="img-fluid">
          </div>
		  </div>
			<p>Hundreds of planets have been discovered using this method. However, only big planets—like Jupiter, or even larger—can be seen this way. Smaller Earth-like planets are much harder to find because they create only small wobbles that are hard to detect.</p>
          </div>
        </div>
		<div class="row">
          <div class="col-md-4" data-aos="fade-up" data-aos-delay="400"><span class="text-primary">How are earthlike planets in other solar systems found?</span></div>
          <div class="col-md-6" data-aos="fade-up" data-aos-delay="500">
            <p>In 2009, NASA launched a spacecraft called Kepler to look for exoplanets. Kepler looked for planets in a wide range of sizes and orbits. And these planets orbited around stars that varied in size and temperature.</p>
			<p>Some of the planets discovered by Kepler are rocky planets that are at a very special distance from their star. This sweet spot is called the habitable zone, where life might be possible.</p>
            <p>Kepler detected exoplanets using something called the transit method. When a planet passes in front of its star, it’s called a transit. As the planet transits in front of the star, it blocks out a little bit of the star's light. That means a star will look a little less bright when the planet passes in front of it.</p>
				<div class="row mb-5">
          <div class="col-12" data-aos="fade-up" data-aos-delay="300">
            <img src="images/kepler.jpg" alt="Image" class="img-fluid">
          </div>
		  </div>
		  <p>Astronomers can observe how the brightness of the star changes during a transit. This can help them figure out the size of the planet.</p>
		  <p>By studying the time between transits, astronomers can also find out how far away the planet is from its star. This tells us something about the planet’s temperature. If a planet is just the right temperature, it could contain liquid water—an important ingredient for life.</p>
		  <div class="row mb-5">
          <div class="col-12" data-aos="fade-up" data-aos-delay="300">
            <img src="images/exo1.jpg" alt="Image" class="img-fluid">
          </div>
		  </div>
		  <p>So far, thousands of planets have been discovered by the Kepler mission. And more will be found by NASA's Transiting Exoplanet Survey Satellite (TESS) mission, which is observing the entire sky to locate planets orbiting the nearest and brightest stars.</p>
		  <p>We now know that exoplanets are very common in the universe. And future NASA missions have been planned to discover many more!</p>
